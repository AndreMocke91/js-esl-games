function lolface()
{
	$("#faceoverlay").removeClass();
	if(!$("#faceoverlay").hasClass("lolface"))
	{
		$("#faceoverlay").addClass("lolface");
		$("#faceoverlay").addClass("flip");
	}
	else
		$("#faceoverlay").removeClass("lolface");
}

